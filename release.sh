# ---- 8. Assemble metadata + artifact manifest ---------------------------
step "Aggregating release-logs/${TAG_LABEL}/"

# Artifact rows: file path | name | size | sha256
build_artifact_row() {
    local name="$1" path="$2"
    [ ! -f "$path" ] && return
    printf '{"name":"%s","path":"%s","size":"%s","sha256":"%s"}' \
        "$name" "$path" "$(human_size "$path")" "$(sha256_file "$path")"
}

ARTIFACTS_JSON="$(build_artifact_row 'native (host)'   bin/vad)"
for arch in amd64 arm64; do
    f="out/${arch}/vad"
    [ -f "$f" ] && ARTIFACTS_JSON="${ARTIFACTS_JSON},$(build_artifact_row "fat-${arch} (linux/${arch})" "$f")"
done

# Image digests
IMG_DIGESTS=""
for arch in amd64 arm64; do
    img="${IMAGE_NAME}:${arch}"
    if digest=$(docker image inspect -f '{{.Id}}' "$img" 2>/dev/null); then
        sep=""; [ -n "$IMG_DIGESTS" ] && sep=","
        size=$(docker images --format '{{.Size}}' "$img" | head -1)
        IMG_DIGESTS="${IMG_DIGESTS}${sep}{\"name\":\"image: ${img}\",\"path\":\"${img}\",\"size\":\"${size}\",\"sha256\":\"${digest##sha256:}\"}"
    fi
done
[ -n "$IMG_DIGESTS" ] && ARTIFACTS_JSON="${ARTIFACTS_JSON},${IMG_DIGESTS}"

# Log file list (everything ending in .log in $LOG_DIR)
LOGS_JSON=""
for f in "$LOG_DIR"/*.log; do
    [ ! -f "$f" ] && continue
    base=$(basename "$f")
    sep=""; [ -n "$LOGS_JSON" ] && sep=","
    LOGS_JSON="${LOGS_JSON}${sep}{\"name\":\"${base}\",\"path\":\"${base}\"}"
done

cat > "$LOG_DIR/metadata.json" <<EOF
{
  "tag":           "${TAG_LABEL}",
  "git_sha":       "${GIT_SHA}",
  "git_branch":    "${GIT_BRANCH}",
  "date":          "$(nowiso)",
  "build_host":    "$(hostname -s)",
  "build_os_arch": "$(uname -s)/$(uname -m)",
  "build_user":    "${USER:-unknown}",
  "build_started": "${BUILD_STARTED}",
  "artifacts":     [${ARTIFACTS_JSON}],
  "validations":   [${VALIDATIONS_JSON}],
  "logs":          [${LOGS_JSON}]
}
EOF

# ---- 9. Review gate -----------------------------------------------------
if [ "$SKIP_REVIEW" = 0 ]; then
    step "Opening release review (browser; waiting for ACCEPT)"
    if ! "$LOG_DIR/release-review" -logs "$LOG_DIR"; then
        echo "  CANCELLED. Nothing pushed. release-logs/${TAG_LABEL}/ preserved."
        exit 1
    fi
else
    step "Review skipped (--skip-review) — proceeding as if ACCEPTED"
fi

echo "release ACCEPTED at $(nowiso)" > "$LOG_DIR/release_decision.log"
echo "validations: $(echo "$VALIDATIONS_JSON" | tr ',' '\n' | grep -c '"ok":true') OK / $(echo "$VALIDATIONS_JSON" | tr ',' '\n' | grep -c '"ok":false') FAIL" >> "$LOG_DIR/release_decision.log"

# ---- 10. Zip the release bundle ----------------------------------------
step "Building release zip"
ZIP_NAME="release_${TAG_LABEL}_${GIT_SHA}.zip"
rm -f "$ZIP_NAME"
# Include logs + the on-disk binary artifacts (not the container images;
# those live in the registry once pushed).
zip -qr "$ZIP_NAME" "$LOG_DIR" bin/vad out/
echo "  built: $ZIP_NAME ($(human_size "$ZIP_NAME"))"

# ---- 11. Push (multi-arch manifest) ------------------------------------
if [ "$PUSH" = 1 ]; then
    step "Push multi-arch manifest to $REGISTRY"
    MANIFEST_TAG="${REGISTRY}:${TAG_LABEL}"
    LATEST_TAG="${REGISTRY}:latest"
    docker buildx build --platform "$(echo "$ARCHES" | tr ' ' ',')" --target runtime \
        -t "$MANIFEST_TAG" -t "$LATEST_TAG" --push . 2>&1 | tee "$LOG_DIR/11_push.log"
    echo "  pushed: $MANIFEST_TAG and $LATEST_TAG"
fi

# TODO: cosign sign goes HERE (after push):
#   cosign sign --yes "$MANIFEST_TAG"
#   cosign sign --yes "$LATEST_TAG"
# Log receipts to $LOG_DIR/12_cosign.log.

# ---- 12. GitHub release ------------------------------------------------
if [ "$RELEASE" = 1 ]; then
    step "GitHub release ${TAG}"
    if git rev-parse "$TAG" >/dev/null 2>&1; then
        echo "  tag $TAG already exists; skipping git tag"
    else
        git tag -a "$TAG" -m "Release $TAG"
        git push origin "$TAG"
    fi
    if command -v gh >/dev/null; then
        gh release create "$TAG" \
            --title "$TAG" \
            --generate-notes \
            "$ZIP_NAME" bin/vad out/amd64/vad out/arm64/vad
        echo "  created: gh release $TAG"
    else
        echo "  gh CLI not installed — skipping release creation."
        echo "  Manual upload: $ZIP_NAME plus the binaries."
    fi
fi

# ---- summary ----------------------------------------------------------
echo ""
echo "=== Release artifacts (${TAG_LABEL}) ==="
echo "Native:"
echo "  bin/vad                       $(human_size bin/vad)"
echo "Standalone Linux binaries:"
for f in out/*/vad; do [ -f "$f" ] && echo "  $f                $(human_size $f)"; done
echo "Slim runtime containers:"
docker images "${IMAGE_NAME}" --format "  {{.Repository}}:{{.Tag}}  {{.Size}}"
echo "Zip:    $ZIP_NAME ($(human_size "$ZIP_NAME"))"
echo "Logs:   $LOG_DIR/"
echo ""
echo "OK"
